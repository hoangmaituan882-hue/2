export function ThemeLanguageProvider({ children }: { children: ReactNode }) {
  const [theme, setTheme] = useLocalStorage<Theme>("app-theme", "light");
  const [language, setLanguage] = useLocalStorage<Language>("app-language", "zh");

  useEffect(() => {
    const root = window.document.documentElement;
    root.classList.remove("light", "dark");
    root.classList.add(theme);
  }, [theme]);

  const toggleTheme = () => {
    setTheme((prev) => (prev === "light" ? "dark" : "light"));
  };

  const toggleLanguage = () => {
    const list: Language[] = ["zh", "ja", "en"];
    setLanguage((prev) => {
      const nextIndex = (list.indexOf(prev) + 1) % list.length;
      return list[nextIndex];
    });
  };

  const t = (key: TranslationKey) => {
    return translations[language]?.[key] || translations["zh"][key];
  };

  return (
    <ThemeLanguageContext.Provider value={{ theme, language, toggleTheme, toggleLanguage, setTheme, setLanguage, t }}>
      {children}
    </ThemeLanguageContext.Provider>
  );
}

export function useThemeLanguage() {
  const context = useContext(ThemeLanguageContext);
  if (context === undefined) {
    throw new Error("useThemeLanguage must be used within a ThemeLanguageProvider");
  }
  return context;
}
